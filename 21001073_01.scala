object tute_02 extends App{

	var k = 2
	var i = 5
	var j = 2
	var m = 5
	var n = 5
	var f = 12.0
	var g = 4.0
	var c = 'X'
	
	printf("k = %d\n", k)
	printf("i = %d\n", i)
	printf("j = %d\n", j)
	printf("m = %d\n", m)
	printf("n = %d\n", n)
	printf("f = %f\n", f)
	printf("g = %f\n", g)
	printf("c = %c\n\n", c)
	
	printf("k + 12 * m =  %d\n", k + 12 * m)
	printf("m / j = %d\n", m / j)
	printf("n %% j= %d\n", n % j)
	printf("m / j * j = %d\n", m / j * j)
	printf("f + 10*5 +g = %f\n", f + 10*5 +g)
	printf("++i * n = %d\n", (1 + i) * n)
	
